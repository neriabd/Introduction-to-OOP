package pepse.world;

/**
 * Represents the movement states of the avatar in the game.
 * @author Neriya Ben David
 */
public enum AvatarMovementState {
    /**
     * When the avatar is idle.
     */
    IDLE,

    /**
     * When the avatar is jumping.
     */
    JUMP,

    /**
     * When the avatar is running.
     */
    RUN
}


